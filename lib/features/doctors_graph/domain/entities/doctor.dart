class DoctorEntity{
  final String name;
  final String email;
  final String mobile;
  final String address;
  final String category;
  final double price;
  final String biography;
  final String cities;

  DoctorEntity({
    required this.name,
    required this.email,
    required this.mobile,
    required this.address,
    required this.category,
    required this.price,
    required this.biography,
    required this.cities,
  });
}
